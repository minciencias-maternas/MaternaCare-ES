#!/usr/bin/env python3
"""Evaluate model predictions against the held-out QA test split.

This evaluates generated model outputs, not the synthetic dataset artifact.
It accepts JSONL generated by either:

- scripts/inference_base.py
- scripts/inference_qlora.py

The evaluator writes one JSONL row per scored prediction as soon as it finishes,
so long runs can be interrupted and resumed safely.

Example:
    python scripts/evaluate_model_predictions.py \
        --input outputs/gemma4-base/test_predictions.jsonl \
        --output outputs/gemma4-base/test_eval.json \
        --limit 25
"""

from __future__ import annotations

import argparse
import asyncio
import json
import statistics
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


DEFAULT_METRICS = (
    "faithfulness",
    "answer_relevancy",
    "answer_correctness",
    "semantic_similarity",
)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Evalúa predicciones de modelos base o fine-tuned sobre el split test."
        )
    )
    parser.add_argument("--input", type=Path, required=True, help="JSONL de predicciones.")
    parser.add_argument(
        "--output",
        type=Path,
        default=None,
        help=(
            "Reporte JSON de resumen. Por defecto usa "
            "<input_stem>_eval.json junto al input."
        ),
    )
    parser.add_argument(
        "--output-jsonl",
        type=Path,
        default=None,
        help=(
            "Detalle incremental JSONL. Por defecto usa el mismo nombre de "
            "--output pero con extensión .jsonl."
        ),
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=None,
        help="Evalúa solo los primeros N ejemplos del input.",
    )
    parser.add_argument("--timeout-seconds", type=int, default=180)
    parser.add_argument(
        "--llm-model",
        type=str,
        default="gpt-5.4-mini",
        help="Modelo OpenAI usado por métricas Ragas basadas en LLM.",
    )
    parser.add_argument(
        "--embedding-model",
        type=str,
        default="text-embedding-3-small",
        help="Modelo de embeddings usado por métricas semánticas.",
    )
    parser.add_argument(
        "--max-completion-tokens",
        type=int,
        default=2048,
        help=(
            "Límite de salida para el juez LLM. Aumentarlo reduce errores "
            "IncompleteOutputException en respuestas clínicas largas."
        ),
    )
    parser.add_argument(
        "--metrics",
        nargs="+",
        default=list(DEFAULT_METRICS),
        choices=DEFAULT_METRICS,
        help="Métricas a calcular.",
    )
    parser.add_argument(
        "--resume",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Reanuda desde --output-jsonl si existe. Usa --no-resume para recalcular.",
    )
    parser.add_argument(
        "--estimate-only",
        action="store_true",
        help="Solo estima volumen aproximado; no llama a OpenAI/Ragas.",
    )
    return parser.parse_args()


def load_local_env() -> None:
    try:
        from dotenv import load_dotenv
    except ImportError:
        return

    project_root = Path(__file__).resolve().parents[1]
    load_dotenv(project_root / ".env")


def default_summary_output(input_path: Path) -> Path:
    return input_path.with_name(f"{input_path.stem}_eval.json")


def default_detail_output(summary_output: Path) -> Path:
    return summary_output.with_suffix(".jsonl")


def read_jsonl(path: Path) -> list[dict[str, Any]]:
    return [
        json.loads(line)
        for line in path.read_text(encoding="utf-8").splitlines()
        if line.strip()
    ]


def load_existing_results(path: Path) -> list[dict[str, Any]]:
    if not path.exists():
        return []
    return read_jsonl(path)


def row_id(row: dict[str, Any], fallback: int) -> str:
    value = row.get("id") or row.get("qa_id") or row.get("metadata", {}).get("qa_id")
    return str(value if value is not None else fallback)


def processed_ids(rows: list[dict[str, Any]]) -> set[str]:
    ids: set[str] = set()
    for idx, row in enumerate(rows, start=1):
        value = row.get("qa_id") or row.get("id") or row.get("metadata", {}).get("qa_id")
        if value is not None:
            ids.add(str(value))
        else:
            ids.add(str(idx))
    return ids


def extract_metric_value(result: Any) -> float | None:
    if isinstance(result, (int, float)):
        return float(result)
    if isinstance(result, dict):
        value = result.get("value")
    else:
        value = getattr(result, "value", None)
    return None if value is None else float(value)


def extract_metric_reason(result: Any) -> str:
    if isinstance(result, dict):
        return str(result.get("reason", "") or "")
    return str(getattr(result, "reason", "") or "")


def get_field(row: dict[str, Any], canonical: str, legacy: str | None = None) -> str:
    value = row.get(canonical)
    if value is None and legacy:
        value = row.get(legacy)
    return str(value or "").strip()


def configure_llm_args(llm: Any, model_name: str, max_completion_tokens: int) -> None:
    model_lower = model_name.lower()
    if not hasattr(llm, "model_args") or not isinstance(llm.model_args, dict):
        return

    if model_lower.startswith("gpt-5."):
        llm.model_args.pop("max_tokens", None)
        llm.model_args["max_completion_tokens"] = max_completion_tokens
        llm.model_args["temperature"] = 1.0
        llm.model_args.pop("top_p", None)
    else:
        llm.model_args["max_tokens"] = max_completion_tokens


def import_ragas_stack() -> dict[str, Any]:
    try:
        from openai import AsyncOpenAI
        from ragas.embeddings.base import embedding_factory
        from ragas.llms import llm_factory
        from ragas.metrics.collections import (
            AnswerCorrectness,
            AnswerRelevancy,
            Faithfulness,
            SemanticSimilarity,
        )
    except ImportError as exc:
        raise SystemExit(
            "Faltan dependencias. Ejecuta: python -m pip install -r requirements.txt"
        ) from exc

    return {
        "AsyncOpenAI": AsyncOpenAI,
        "embedding_factory": embedding_factory,
        "llm_factory": llm_factory,
        "AnswerCorrectness": AnswerCorrectness,
        "AnswerRelevancy": AnswerRelevancy,
        "Faithfulness": Faithfulness,
        "SemanticSimilarity": SemanticSimilarity,
    }


def build_metrics(
    metric_names: list[str],
    llm_model: str,
    embedding_model: str,
    max_completion_tokens: int,
    stack: dict[str, Any],
) -> dict[str, Any]:
    client = stack["AsyncOpenAI"]()
    llm = stack["llm_factory"](llm_model, client=client)
    configure_llm_args(llm, llm_model, max_completion_tokens)
    embeddings = stack["embedding_factory"](
        "openai", model=embedding_model, client=client
    )

    metrics: dict[str, Any] = {}
    if "faithfulness" in metric_names:
        metrics["faithfulness"] = stack["Faithfulness"](llm=llm)
    if "answer_relevancy" in metric_names:
        metrics["answer_relevancy"] = stack["AnswerRelevancy"](
            llm=llm, embeddings=embeddings
        )
    if "answer_correctness" in metric_names:
        metrics["answer_correctness"] = stack["AnswerCorrectness"](
            llm=llm, embeddings=embeddings
        )
    if "semantic_similarity" in metric_names:
        metrics["semantic_similarity"] = stack["SemanticSimilarity"](
            embeddings=embeddings
        )
    return metrics


async def score_prediction(
    row: dict[str, Any],
    qa_id: str,
    metrics: dict[str, Any],
    timeout_seconds: int,
) -> dict[str, Any]:
    question = get_field(row, "question", "pregunta")
    generated = get_field(row, "generated_answer", "generated")
    reference = get_field(row, "reference_answer", "respuesta")
    context = get_field(row, "source_context", "contexto_fuente")

    scored: dict[str, Any] = {
        "qa_id": qa_id,
        "model_role": row.get("model_role"),
        "model_name": row.get("model_name"),
        "adapter_dir": row.get("adapter_dir"),
        "dataset_variant": row.get("dataset_variant"),
        "question": question,
        "generated_answer": generated,
        "reference_answer": reference,
        "source_context": context,
        "metadata": row.get("metadata", {}),
    }
    errors: list[str] = []

    metric_calls: dict[str, Any] = {}
    if "faithfulness" in metrics:
        metric_calls["faithfulness"] = metrics["faithfulness"].ascore(
            user_input=question,
            response=generated,
            retrieved_contexts=[context] if context else [],
        )
    if "answer_relevancy" in metrics:
        metric_calls["answer_relevancy"] = metrics["answer_relevancy"].ascore(
            user_input=question,
            response=generated,
        )
    if "answer_correctness" in metrics:
        metric_calls["answer_correctness"] = metrics["answer_correctness"].ascore(
            user_input=question,
            response=generated,
            reference=reference,
        )
    if "semantic_similarity" in metrics:
        metric_calls["semantic_similarity"] = metrics["semantic_similarity"].ascore(
            response=generated,
            reference=reference,
        )

    for name, metric_call in metric_calls.items():
        try:
            metric_result = await asyncio.wait_for(metric_call, timeout=timeout_seconds)
            scored[name] = extract_metric_value(metric_result)
            reason = extract_metric_reason(metric_result)
            if reason:
                scored[f"{name}_reason"] = reason
        except Exception as exc:
            scored[name] = None
            errors.append(f"{name}: {type(exc).__name__} {exc!r}")

    if errors:
        scored["error"] = " | ".join(errors)
    return scored


async def evaluate_predictions(
    rows: list[dict[str, Any]],
    existing_rows: list[dict[str, Any]],
    output_jsonl: Path,
    metric_names: list[str],
    llm_model: str,
    embedding_model: str,
    max_completion_tokens: int,
    timeout_seconds: int,
) -> list[dict[str, Any]]:
    stack = import_ragas_stack()
    metrics = build_metrics(
        metric_names, llm_model, embedding_model, max_completion_tokens, stack
    )

    done = processed_ids(existing_rows)
    results = list(existing_rows)
    total = len(rows)
    skipped = 0
    new_count = 0

    output_jsonl.parent.mkdir(parents=True, exist_ok=True)
    with open(output_jsonl, "a", encoding="utf-8", newline="\n") as f:
        for idx, row in enumerate(rows, start=1):
            qa_id = row_id(row, idx)
            if qa_id in done:
                skipped += 1
                continue

            print(f"[EVAL] {idx}/{total} qa_id={qa_id}", flush=True)
            scored = await score_prediction(row, qa_id, metrics, timeout_seconds)
            f.write(json.dumps(scored, ensure_ascii=False) + "\n")
            f.flush()
            done.add(qa_id)
            results.append(scored)
            new_count += 1

    print(f"[EVAL] nuevos={new_count} saltados={skipped}", flush=True)
    return results


def mean_score(rows: list[dict[str, Any]], key: str) -> float | None:
    values = [row[key] for row in rows if row.get(key) is not None]
    return round(statistics.mean(values), 4) if values else None


def summarize(results: list[dict[str, Any]], metric_names: list[str]) -> dict[str, Any]:
    summary: dict[str, Any] = {
        "predictions_evaluated": len(results),
        "predictions_with_error": sum(1 for row in results if row.get("error")),
    }
    for metric_name in metric_names:
        summary[f"avg_{metric_name}"] = mean_score(results, metric_name)
    return summary


def estimate_tokens(text: str) -> int:
    # Cheap approximation for Spanish/English mixed text. Good enough for
    # budget planning, not billing-grade accounting.
    return max(1, round(len(text) / 4))


def estimate_volume(rows: list[dict[str, Any]]) -> dict[str, Any]:
    per_row = []
    for row in rows:
        combined = "\n".join(
            [
                get_field(row, "question", "pregunta"),
                get_field(row, "generated_answer", "generated"),
                get_field(row, "reference_answer", "respuesta"),
                get_field(row, "source_context", "contexto_fuente"),
            ]
        )
        per_row.append(estimate_tokens(combined))
    return {
        "rows": len(rows),
        "approx_content_tokens": sum(per_row),
        "approx_avg_content_tokens_per_row": round(statistics.mean(per_row), 1)
        if per_row
        else 0,
        "note": (
            "Ragas agrega prompts internos por métrica; el costo real suele ser "
            "varias veces este volumen base."
        ),
    }


def write_summary(
    output: Path,
    input_path: Path,
    output_jsonl: Path,
    rows: list[dict[str, Any]],
    results: list[dict[str, Any]],
    args: argparse.Namespace,
) -> None:
    report = {
        "script_version": "model_prediction_eval_v2_resumable",
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "input_file": str(input_path),
        "detail_file": str(output_jsonl),
        "sampled_predictions": len(rows),
        "metrics": args.metrics,
        "ragas_models": {
            "llm_model": args.llm_model,
            "embedding_model": args.embedding_model,
            "max_completion_tokens": args.max_completion_tokens,
        },
        "summary": summarize(results, args.metrics),
    }
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(
        json.dumps(report, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"[EVAL] Resumen guardado en: {output}", flush=True)
    print(f"[EVAL] Detalle guardado en: {output_jsonl}", flush=True)


async def main_async(args: argparse.Namespace) -> None:
    rows = read_jsonl(args.input)
    if args.limit is not None:
        rows = rows[: args.limit]

    output = args.output or default_summary_output(args.input)
    output_jsonl = args.output_jsonl or default_detail_output(output)

    if args.estimate_only:
        print(json.dumps(estimate_volume(rows), ensure_ascii=False, indent=2))
        return

    if output_jsonl.exists() and not args.resume:
        output_jsonl.unlink()
        print(f"[EVAL] Detalle previo eliminado por --no-resume: {output_jsonl}")

    existing_rows = load_existing_results(output_jsonl) if args.resume else []
    if existing_rows:
        print(f"[EVAL] Reanudando: {len(existing_rows)} filas ya evaluadas")

    results = await evaluate_predictions(
        rows=rows,
        existing_rows=existing_rows,
        output_jsonl=output_jsonl,
        metric_names=args.metrics,
        llm_model=args.llm_model,
        embedding_model=args.embedding_model,
        max_completion_tokens=args.max_completion_tokens,
        timeout_seconds=args.timeout_seconds,
    )
    write_summary(output, args.input, output_jsonl, rows, results, args)


def main() -> None:
    load_local_env()
    args = parse_args()
    asyncio.run(main_async(args))


if __name__ == "__main__":
    main()
